//! power: 省電力（スリープ）対応の設計メモと最小フック
//!
//! 責務: 「どこでスリープできるか」を構造として示すこと。実スリープはしない。
//! 依存方向: どのモジュールにも依存しない。
//!
//! # なぜこのプロジェクトでは実スリープしないのか
//!
//! ボタン端末は電池駆動が想定されるため、本来はハートビート送信の合間
//! （約500msの待ち時間）にlight sleepへ入れると消費電力を大きく減らせます。
//! しかし単純にスリープすると次の問題が起きます。
//!
//! - スリープ中はWi-Fi（ESP-NOW）の受信回路も止まるため、受信側からの
//!   ACKや将来の下り通知を取りこぼす（受信待ちとスリープの両立問題）
//! - ESP-NOWにはPeerのwake window設定（EspNowManager::set_wake_window）など
//!   「起きている時間帯を揃える」仕組みがあり、これと送信周期の設計を
//!   セットで考える必要がある
//! - Embassyのタイマ駆動とRTCスリープからの復帰時刻合わせも必要になる
//!
//! この「受信待ちとスリープの両立設計」は12章（sleep）で解説しています。
//! ここでは将来スリープを入れられるよう、呼び出し位置（フック）だけを
//! アプリの構造に用意しておきます。

